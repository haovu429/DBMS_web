package entity;

public class LineItem {
	private int pid;
	private int quantity;
	
	public LineItem() {
		
	}
	
	public LineItem(int pid, int quantity) {
		
	}

	public int getPid() {
		return pid;
	}

	public void setPid(int pid) {
		this.pid = pid;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	

}
